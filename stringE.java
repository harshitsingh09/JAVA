public class stringE {
    public static void main(String[] args) {
        String s1 = "REVA"; //creating a string using Literal method
        char ch[] = {'u','n','i','v','e','r','s','i','t','y'};
        String s2 = new String(ch); // Creating string using NEW keyword
        String s3 = new String("BENGALURU"); // NEW keyword creates a new object in String class
        System.out.println(s1);
        System.out.println(s2);
        System.out.println(s3);
    }
}
